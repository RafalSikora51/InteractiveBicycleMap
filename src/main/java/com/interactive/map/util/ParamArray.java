package com.interactive.map.util;

import java.util.List;

public class ParamArray {

	private List<Integer> pressedNodes;
	private List<Integer> recycledNodes;

	
	public List<Integer> getPressedNodes() {
		return pressedNodes;
	}

	public void setPressedNodes(List<Integer> pressedNodes) {
		this.pressedNodes = pressedNodes;
	}

	public List<Integer> getRecycledNodes() {
		return recycledNodes;
	}

	public void setRecycledNodes(List<Integer> recycledNodes) {
		this.recycledNodes = recycledNodes;
	}

}
